let puntaje = 5;
//lo manda a llamar y luego lo incrementa
puntaje++; //5
console.log(puntaje);
//lo incrementa y luego lo muestra
++puntaje; //6
console.log(puntaje);
//lo muestra y luego lo resta
puntaje--; //5
console.log(puntaje);
//lo resta y luego lo muestra
--puntaje; //4
console.log(puntaje);
//al resultado acumulado le incrementa +3
puntaje += 3;
console.log(puntaje);
//al resultado acumulado le resta -3
puntaje -= 3;
console.log(puntaje);